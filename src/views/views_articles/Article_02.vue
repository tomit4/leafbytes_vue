                <p class="article-content">
                    And do more research, but I'll give you a general idea of why this is such a powerful tool for anyone invested in computers to know. Essentially aliases are a series of commands invoked by a custom command of your naming.
                </p>
                <p class="article-content">
                    If you invoke:
                </p>
                <pre><code class="language-bash">[~]$ alias cdls="cd ~/Documents & ls"</code></pre>
                <p class="article-content">
                    You will navigate into your Documents directory and then list out its contents after invoking your custom command, 'cdls'
                </p>
                <p class="article-content">
                    The '&' is another operator that waits for one command to complete before then invoking the following command.The '&' operator can be chained as many times as desired and when applied to an alias, essentially condenses a very long command into a shorter command (unless you name your alias something very long I suppose).
                </p>
                <p class="article-content">
                    Now, the somewhat annoying thing is that these aliases, when created this way, are forgotten as soon as you close the terminal or your shell session somehow ends. Aliases can be saved, however, by placing them in your ~/.bashrc file. This configuration file is checked by the shell prior to initiation, including alias assignments. Thusly, if you simply append:
                </p>
                <pre><code class="language-bash">[~]$ alias cdls="cd ~/Documents & ls"</code></pre>
                <p class="article-content">
                    To the end of your ~/.bashrc file, you will have created a custom command that persists even after you have closed your terminal and shut down your computer.
                </p>
                <p class="article-content">
                    This is the very essentials of scripting, the chaining of programs together to create different results. An alias is essentially a one line script.
                </p>
