                </p>
                <pre><code class="language-bash">[~]$ alias tshift="sudo timeshift --delete && sudo timeshift --create && sudo timeshift --list"</code></pre>
                <p class="article-content">
                    As you can see, tshift is far less characters to type, and it is a very handy, but very simple, alias I have created for myself. (if you are on Linux and unfamiliar with the sudo command, please see the man pages for sudo, if you are on MacOs, a simple query into your favorite search engine I am sure will yield good results).
