                    The reason I point this out is that even though timeshift isn't a part of the shell as a subject, I am simply pointing it out as a practical example of when utilizing the text interface of Timeshift's CLI, via the shell, became more useful to me than interfacing with the GUI version. This became even more the case when I discovered the existence of shell aliases.
                </p>
                <p class="article-content">
                    Of course, I've provided you now with the knowledge of how to do your own research regarding any command, so be sure to pull up:
                </p>
